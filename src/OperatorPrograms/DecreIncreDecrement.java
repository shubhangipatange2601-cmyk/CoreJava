package OperatorPrograms;

public class DecreIncreDecrement {
    //Programs Using Both Increment and Decrement Operators
    public static void main(String[] args) {
        int s = 1290;
        int g = 300;
        System.out.println("PostIncrement:"+(s++));
        System.out.println("PreIncrement: "+(++s));
        System.out.println("PostDecrement: "+(g--));
        System.out.println("PreDecrement:"+(--g));


        int h = 2000;
        int i = 100;
        System.out.println("PostIncrement:"+(h++));
        System.out.println("PreIncrement:"+(++h));
        System.out.println("PostDecrement:"+(i--));
        System.out.println("PreDecrement:"+(--i));


    }



}
